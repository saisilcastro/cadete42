char	*ft_strcat(char *dest, char *src)
{
	int	i, j;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[i] != '\0')
	{
		dest[i + j] = src[j];
	}
	dest[i] = '\0';
	return (dest);
}
