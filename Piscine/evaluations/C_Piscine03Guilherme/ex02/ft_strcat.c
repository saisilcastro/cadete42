char	*ft_strcat(char *dest, char *src)
{
	int	x;
	int	i;

	x = 0;
	i = 0;
	while (dest[x] != '\0')
		x++;
	while (src[i] != '\0')
	{
		dest[x + i] = src[i];
		i++;
	}
	dest[x] = '\0';
	return (dest);
}
