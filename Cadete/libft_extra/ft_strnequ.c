#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int	i;

	i = 0;
	(void)n;
	while (*(s1 + i) && *(s2 + i))
	{
		if (*(s1 + i) != *(s2 + i))
			return (1);
		i++;
	}
	if ((*(s1 + i) && !*(s2 + i))
		|| (!*(s1 + i) && *(s2 + i)))
		return (1);
	return (0);
}
